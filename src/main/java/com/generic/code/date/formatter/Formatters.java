package com.generic.code.date.formatter;

/**
 * Constant definitions for formatters for parsing date-time objects.
 */
public final class Formatters {

  /**
   * constructor.
   */
  private Formatters() {}

  /**
   * The ISO date-time formatter without an offset, such as '2011-12-03T10:15:30.123'.
   */
  public static final String ISO_LOCAL_DATE_TIME = "yyyy-MM-dd'T'HH:mm:ss.SSS";
}
