package com.generic.code.system;

/**
 * Utility class for using {@link java.lang.System}.
 */
public final class SystemUtil {

  /**
   * constructor.
   */
  private SystemUtil() {}

  /**
   * Prints an object.
   *
   * @param x the Object to be printed.
   */
  public static void print(Object x) {
    System.out.println(x);
  }
}
